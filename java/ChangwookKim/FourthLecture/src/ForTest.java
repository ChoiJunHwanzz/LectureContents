public class ForTest {
    public static void main(String[] args) {
        // for문의 구성
        // for(초기화 코드; 조건식 코드; 증감식 코드)로 구성됩니다.
        //초기화 코드는 for문에 최초 진입시 한 번만 실행합니다.

        // 1부터 시작해서 10이하까지만 출력하게끔 함
        // 딱히 이해 안가는 부분은 없는듯
        for(int i = 1; i <= 10; i++) {
            System.out.println("i = " +i);
        }

    }
}
