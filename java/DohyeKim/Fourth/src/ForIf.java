public class ForIf {
    public static void main(String[] args) {
        for(int i =1; i <=30;  i++){
            if(i % 2 == 0){
                System.out.println("짝수입니다");
            }else{
                System.out.println("홀수입니다");
            }
        }
        System.out.println();
    }
}
