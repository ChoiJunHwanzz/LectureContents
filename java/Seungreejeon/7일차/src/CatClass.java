public class CatClass {
    String name;
    String catBreed;
    int age;

    String getName() {
        return name;
    }
    void setName(String name) {
        this.name = name;
    }
    String getCatBreed() {
        return catBreed;
    }
    void setCatBreed(String catBreed) {
        this.catBreed = catBreed;
    }
    int getAge() {
        return age;
    }
    void setAge(int age) {
        this.age = age;
    }
}
