public class ForIfTest {
    public static void main(String[] args) {
            for(int i=1; i<=30; i++) {
                if(i%2 == 0) {
                    System.out.println(i+"는 짝수");
                }
                else {
                    System.out.println(i+"는 홀수");
                }
            }
    }
}
