public class WhileTest {
    public static void main(String[] args) {
        // 가장 기본적인 while 반복문의 형태
        // while 반복문 작성법
        // while(조건){조건이 만족하는 동안 동작시킬 코드 작성}

        // false를 직접 입력하면 루프 내부에 도달할 수 없음을 알리는 에러메세지 나옴

        //모든 제어문(if, for, while 등)에서 사용하는 조건은 반드시 결과가 true or false로 나타남
        while(true) {
            //조건이 계속 true이므로 밑의 코드가 무한반복하게 됨
            System.out.println("ㅎㅇ 정수리 n번 맞으면 죽을 수 있대");
        }
    }
}
