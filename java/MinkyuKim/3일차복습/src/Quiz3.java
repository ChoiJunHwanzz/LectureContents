public class Quiz3 {
    public static void main(String[] args) {
        // if를 활용하여 숫자 3이 짝수인지 홀수인지 판별
        int num = 3;
        if (num % 2 == 0) {
            System.out.println("이 숫자는 2의 배수(짝수)입니다.");
        } else {
            System.out.println("이 숫자는 2의 배수가 아닙니다(홀수)");
        }
    }
}
