public class _99th_Practice {
    public static void main(String[] args) {
        for (char ch = 'A'; ch < 'Z'; ch++) {
            System.out.print(ch);
            if (ch == 'O') {
                System.out.println();
            }
        }
        System.out.println();
        for (char k = 'ㄱ'; k < 'ㅎ'; k++) {
            System.out.print(k);
            if (k == 'ㄾ') {
                System.out.println();
            }
        }
    }
}